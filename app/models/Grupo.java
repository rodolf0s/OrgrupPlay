package models;

public class Grupo {
	
	public Integer id;
	public String nombre;
	public String distintivo;
	
	public void setId(Integer id) {
		this.id = id;
	}
	
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	public void setDistintivo(String distintivo) {
		this.distintivo = distintivo;
	}
}
